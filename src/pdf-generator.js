const fs = require('fs');
const path = require('path');

class PDFReportGenerator {
  constructor(options = {}) {
    this.template = options.template || 'default';
    this.autoFormatting = options.autoFormatting || false;
    this.branding = options.branding || null;
    this.outputDir = options.outputDir || './reports';
  }

  /**
   * Generate PDF report from audit data
   */
  async generateReport(data) {
    const { appName, platform, issues, remediation } = data;

    // Validate input
    if (!appName || !platform || !issues) {
      throw new Error('Missing required fields: appName, platform, issues');
    }

    // Generate HTML content
    const html = this.generateHTML({ appName, platform, issues, remediation });

    // Convert to PDF (simulated - in production use pdfkit, puppeteer, etc.)
    const pdf = await this.convertToPDF(html);

    return {
      appName,
      platform,
      generatedAt: new Date().toISOString(),
      pdf,
      save: async (filename) => {
        const filePath = path.join(this.outputDir, filename);
        await this.savePDF(pdf, filePath);
        return filePath;
      }
    };
  }

  /**
   * Generate HTML content for PDF
   */
  generateHTML(data) {
    const { appName, platform, issues, remediation } = data;

    let html = `
<!DOCTYPE html>
<html>
<head>
  <meta charset="UTF-8">
  <title>Compliance Report - ${appName}</title>
  <style>
    body { font-family: Arial, sans-serif; margin: 40px; }
    h1 { color: #333; border-bottom: 2px solid #007AFF; padding-bottom: 10px; }
    h2 { color: #555; margin-top: 30px; }
    .issue { background: #f9f9f9; padding: 15px; margin: 15px 0; border-left: 4px solid #ff3b30; }
    .severity-high { border-left-color: #ff3b30; }
    .severity-medium { border-left-color: #ff9500; }
    .severity-low { border-left-color: #34c759; }
    .policy-ref { font-size: 12px; color: #666; }
    .remediation { background: #e8f4ff; padding: 15px; margin: 15px 0; }
    .code-suggestion { background: #f5f5f5; padding: 10px; font-family: monospace; font-size: 12px; }
    .footer { margin-top: 50px; text-align: center; font-size: 12px; color: #999; }
  </style>
</head>
<body>
  <h1>Compliance Report</h1>
  <p><strong>App:</strong> ${appName}</p>
  <p><strong>Platform:</strong> ${platform}</p>
  <p><strong>Generated:</strong> ${new Date().toISOString()}</p>

  <h2>Executive Summary</h2>
  <p>Total Issues: ${issues.length}</p>
  <p>High: ${issues.filter(i => i.severity === 'High').length}</p>
  <p>Medium: ${issues.filter(i => i.severity === 'Medium').length}</p>
  <p>Low: ${issues.filter(i => i.severity === 'Low').length}</p>

  <h2>Issues</h2>
`;

    issues.forEach((issue, index) => {
      html += `
  <div class="issue severity-${issue.severity.toLowerCase()}">
    <h3>Issue #${index + 1}: ${issue.title}</h3>
    <p><strong>Severity:</strong> ${issue.severity}</p>
    <p class="policy-ref"><strong>Policy Reference:</strong> ${issue.policyReference}</p>
    <p><strong>Description:</strong> ${issue.description}</p>
    <p><strong>Impact:</strong> ${issue.impact}</p>
    <p><strong>Evidence:</strong> ${issue.evidence}</p>
  </div>
`;
    });

    if (remediation) {
      html += `
  <h2>Remediation Plan</h2>
  <div class="remediation">
    <p><strong>Fix Summary:</strong> ${remediation.fixSummary}</p>
    <h3>Steps to Resolve:</h3>
    <ol>
      ${remediation.steps.map(step => `<li>${step}</li>`).join('')}
    </ol>
    ${remediation.codeSuggestions ? `
    <h3>Code Suggestions:</h3>
    <div class="code-suggestion">${remediation.codeSuggestions.join('\n')}</div>
    ` : ''}
  </div>
`;
    }

    html += `
  <div class="footer">
    <p>Generated by Gracias.sh AI Auditor</p>
    <p>https://opensource.gracias.sh/</p>
  </div>
</body>
</html>
`;

    return html;
  }

  /**
   * Convert HTML to PDF (simulated)
   */
  async convertToPDF(html) {
    // In production, use pdfkit, puppeteer, or similar
    // For now, return simulated PDF data
    return Buffer.from(html);
  }

  /**
   * Save PDF to file system
   */
  async savePDF(pdf, filePath) {
    // Ensure output directory exists
    const dir = path.dirname(filePath);
    if (!fs.existsSync(dir)) {
      fs.mkdirSync(dir, { recursive: true });
    }

    // Save PDF
    fs.writeFileSync(filePath, pdf);
    console.log(`PDF saved to: ${filePath}`);
  }

  /**
   * Set report template
   */
  setTemplate(template) {
    this.template = template;
  }

  /**
   * Enable auto formatting
   */
  enableAutoFormatting() {
    this.autoFormatting = true;
  }

  /**
   * Add company branding
   */
  addBranding(branding) {
    this.branding = branding;
  }
}

module.exports = PDFReportGenerator;

// Example usage
if (require.main === module) {
  (async () => {
    const generator = new PDFReportGenerator({
      outputDir: './output'
    });

    const report = await generator.generateReport({
      appName: 'My App',
      platform: 'iOS',
      issues: [
        {
          title: 'Permission Misuse: Location',
          severity: 'High',
          policyReference: 'https://developer.apple.com/app-store/review/guidelines/#location',
          description: 'App requests location permission without clear justification',
          impact: 'May cause rejection during App Store review',
          evidence: 'NSLocationWhenInUseUsageDescription is vague'
        }
      ],
      remediation: {
        fixSummary: 'Update location permission description',
        steps: [
          'Update Info.plist with clear usage description',
          'Explain why location is needed',
          'Provide examples of location usage'
        ],
        codeSuggestions: [
          '<key>NSLocationWhenInUseUsageDescription</key>',
          '<string>We use your location to show nearby stores and provide location-based recommendations.</string>'
        ]
      }
    });

    await report.save('compliance-report.pdf');
  })();
}
